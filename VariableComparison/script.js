// How operators work with different variable types.
let a = 1;
let b = 2;

console.log(a == b);
console.log(a != b);
console.log(a > b);
console.log(a < b);
console.log(a <= b);
console.log(a >= b);

let c = true;
let d = false;

console.log(c == d);
console.log(c != d);

let e = "a";
let f = "b";

console.log(e == f);
console.log(e != f);
